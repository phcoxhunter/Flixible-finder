<h1><center><b><i><u>Air Pollution</u></i></b></center></h1>
<p><font size="5"><b>Air pollution exposure threatens human health both outdoors and when polluted air infiltrates homes, offices, schools and vehicles. Exposure to certain particulate matter can cause respiratory, cardiovascular and nervous system issues, especially in vulnerable populations. Several presentations at the 2019 Society for Risk Analysis (SRA) Annual Meeting will explore new ways to measure and track air pollutants to reduce public health risk.</b></font></p>
<font size="5">
<p>The first step in reducing exposure is to quantify current exposures to understand when and where people are at the highest risk. H. Christopher Frey, Ph.D., North Carolina State University, is part of a Hong Kong-based team that used portable sensor technology to measure concentrations of air pollutants at 200 locations. The team then used this data to quantify air pollutant exposure outdoors and find out how much ambient air pollution penetrates indoors.</p>
<p>The information collected from the Hong Kong study, "Measurement and modeling of urban personal air pollution exposure in Hong Kong," will be incorporated into a publicly available cellphone app for the Personalized Real-Time Air Quality Informatics System for Exposure -- Hong Kong (PRAISE-HK). The app lets users identify air pollutants to which they are sensitive and identify ways to reduce exposure via the key features including route choice to a particular destination. Users will also be able to receive alerts about locations with high exposures and obtain exposure forecasts for up to 48 hours to aid in planning activities.</p>
<p>In India, 'chalk and talk' is the most common and widespread method of teaching as it enhances interaction between students and teachers, but a great concern associated with this method is the generation of chalk dust. Students and teachers then inhale the dust with negative impacts on their health. Abinaya Sekar, National Institute of Technology Calicut, studied the exposure to chalk dust that teaching professionals face during class.</p>
<p>Sekar's study, "Particulate matter exposure of teaching professionals during a typical chalk and talk class," analyzed chalk dust exposure for 40 teaching professionals in terms of particulate matter in three size ranges -- PM 10, PM 2.5 and PM 1. The study found that exposure varied with respect to the teacher's height. Those in the 5'-5'6" range had maximum exposure while writing in the middle portion of the board. Those who are in the 5'6"-5'9" range had maximum exposure while writing in the top portion.</p>
<p>"This research may provide awareness among the public to limit the usage of chalks by both students and teachers," states Sekar. "This may help policy makers to shift from traditional classrooms to smart classrooms."</p>
<p>Over the past several years, air pollution has been linked to an increased risk of several respiratory diseases in children, especially respiratory tract infections. Elisa Gallo, University of Padova, conducted a study, "Increasing risk of emergency room admissions for bronchiolitis in infants exposed to air pollution," aimed at evaluating the association between and air pollution and pediatric emergency room (ER) admissions for bronchiolitis.</p>
<p>Gallo found that the particulate matter and nitrogen dioxide were associated with ER admissions, although particulate matter was more likely to exacerbate the condition in children already showing mild symptoms. Gallo's research is intended to help inform parents of the risks of poor air quality, since children are very sensitive to lung disease while their immune system is developing, and early exposure can have lifelong consequences. In particular, bronchiolitis has been associated with a higher risk of lifetime wheezing and asthma.</p>
<p>These studies will be presented during the Exposure Assessment of Air Pollutants: New Frontiers in the Assessment of Public Health Risks session on Wednesday, Dec. 11 at the 2019 SRA Annual Meeting at the Crystal Gateway Marriott in Arlington, Virginia.</p></font>
